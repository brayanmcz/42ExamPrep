#include <unistd.h>

int main(void)
{
    char c;
    int i;

    i = 25;
    while (i >= 0)
    {
        if (i % 2 == 0)
            c = i + 'A';
        else
            c = i + 'a';
        write(1, &c, 1);
        i--;
    }
    write(1, "\n", 1);
    return (0);
}