#include <unistd.h>

int main(int argc, char **argv)
{
    char c;
    int i;

    if (argc >= 2)
    {
        i = 0;
        while (argv[1][i] != '\0')
        {
            c = argv[1][i];
            write(1, &c, 1);
            i++;
        }
    }
    write(1, "\n", 1);
    return (0);
}