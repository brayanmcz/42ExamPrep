#include <unistd.h>

int main(void)
{
    char *str;
    char c;
    int i;

    str = "Hello World!";
    i = 0;
    while (str[i] != '\0')
    {
        c = str[i];
        write(1, &c, 1);
        i++;
    }
    write(1, "\n", 1);
    return (0);
}